    return view('home');
})->name('home');
Route::get('/dashboard', [DashboardController::class, 'index'])->name('dashboard'); //->middleware('auth') part makes sure to redirect to login if /dashboard is accessed

Route::get('/users/{user:name}/posts', [UserPostController::class, 'index'])->name('users.posts');   // to view lists of posts for each user

Route::get('/login', [LoginController::class, 'index'])->name('login');
